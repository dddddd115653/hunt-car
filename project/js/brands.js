function createBrands(options){
    Data = options.forEach(function(item){
        var span = createBox("span"),
            div = createBox("div"),
            div1 = createBox("div"),
            aa = item.aa;
        span.innerHTML=item.span;
        apChild(div,span);
        aa.forEach(function(item1){
            var a = createBox("a");
            a.innerText = item1.a;
            a.href = item1.href;
            return apChild(div1,a);
        });
        apChild(div,div1);
        return apChild(Fragment,div);
    });
    apChild(document.getElementById("brands"),Fragment);
}
